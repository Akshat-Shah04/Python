def sort(li):
    return sorted(li)


li = [1.5, 0.4, 9.0, 7.5, 3.6, 11.87, 4.70, 2.56, 1.56]
print("List of Decimal Numbers : ", li)
li = sort(li)
print("Maximum Number of the list is :", li[-1])
print("Minimum Number of the list is :", li[0])

